#creating  a populated list.
#Syntax: list_name = [item1, item2, …]
fruits = ["Orange", "Peach", "Mango", "Plum"]

#looping through entire list and printing the elements
for fruit in fruits:
    print(fruit)

#sorting list in alphabetical order
fruits.sort()

#displaying the sorted list
print(fruits)